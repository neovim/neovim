std::string::const_iterator mystrsep(const std::string &str,
                                     std::string::const_iterator& start) {
  auto end = str.end();

  is_any_of op(" \t");
  // don't use isspace() here, the string can be in some random charset
  // that's way different than the locale's
  auto sp = start;
  while (sp != end && op(*sp))
      ++sp;

  auto dp = sp;
  while (dp != end && !op(*dp))
      ++dp;

  start = dp;
  return sp;
}

// remove cross-platform text line end characters
void mychomp(std::string& s) {
  size_t k = s.size();
  size_t newsize = k;
  if ((k > 0) && ((s[k - 1] == '\r') || (s[k - 1] == '\n')))
    --newsize;
  if ((k > 1) && (s[k - 2] == '\r'))
    --newsize;
  s.resize(newsize);
}

// break text to lines
std::vector<std::string> line_tok(const std::string& text, char breakchar) {
  std::vector<std::string> ret;
  if (text.empty()) {
    return ret;
  }

  std::stringstream ss(text);
  std::string tok;
  while(std::getline(ss, tok, breakchar)) {
    if (!tok.empty()) {
      ret.push_back(tok);
    }
  }

  return ret;
}

// uniq line in place
void line_uniq(std::string& text, char breakchar)
{
  std::vector<std::string> lines = line_tok(text, breakchar);
  text.clear();
  if (lines.empty()) {
    return;
  }
  text = lines[0];
  for (size_t i = 1; i < lines.size(); ++i) {
    bool dup = false;
    for (size_t j = 0; j < i; ++j) {
      if (lines[i] == lines[j]) {
        dup = true;
        break;
      }
    }
    if (!dup) {
      if (!text.empty())
        text.push_back(breakchar);
      text.append(lines[i]);
    }
  }
}

// uniq and boundary for compound analysis: "1\n\2\n\1" -> " ( \1 | \2 ) "
void line_uniq_app(std::string& text, char breakchar) {
  if (text.find(breakchar) == std::string::npos) {
    return;
  }

  std::vector<std::string> lines = line_tok(text, breakchar);
  text.clear();
  if (lines.empty()) {
    return;
  }
  text = lines[0];
  for (size_t i = 1; i < lines.size(); ++i) {
    bool dup = false;
    for (size_t j = 0; j < i; ++j) {
      if (lines[i] == lines[j]) {
        dup = true;
        break;
      }
    }
    if (!dup) {
      if (!text.empty())
        text.push_back(breakchar);
      text.append(lines[i]);
    }
  }

  if (lines.size() == 1) {
    text = lines[0];
    return;
  }

  text.assign(" ( ");
  for (auto& line : lines) {
    text.append(line);
    text.append(" | ");
  }
  text[text.size() - 2] = ')';  // " ) "
}

// append s to ends of every lines in text
std::string& strlinecat(std::string& str, const std::string& apd) {
  size_t pos = 0;
  while ((pos = str.find('\n', pos)) != std::string::npos) {
    str.insert(pos, apd);
    pos += apd.length() + 1;
  }
  str.append(apd);
  return str;
}

int fieldlen(const char* r) {
  int n = 0;
  while (r && *r != ' ' && *r != '\t' && *r != '\0' && *r != '\n') {
    r++;
    n++;
  }
  return n;
}

bool copy_field(std::string& dest,
                const std::string& morph,
                const std::string& var) {
  if (morph.empty())
    return false;
  size_t pos = morph.find(var);
  if (pos == std::string::npos)
    return false;
  dest.clear();
  std::string beg(morph.substr(pos + MORPH_TAG_LEN, std::string::npos));

  for (const char c : beg) {
	if (c == ' ' || c == '\t' || c == '\n')
      break;
    dest.push_back(c);
  }

  return true;
}

std::string& mystrrep(std::string& str,
                      const std::string& search,
                      const std::string& replace) {
  size_t pos = 0;
  while ((pos = str.find(search, pos)) != std::string::npos) {
    str.replace(pos, search.length(), replace);
    pos += replace.length();
  }
  return str;
}

// reverse word
size_t reverseword(std::string& word) {
  std::reverse(word.begin(), word.end());
  return word.size();
}

// reverse word
size_t reverseword_utf(std::string& word) {
  std::reverse(word.begin(), word.end()); //1st step: we reverse the string
	
  size_t num_chars = word.size(); //in order to make sure there are enough characters at the end of the string when we process a multibyte character
  //2nd step: we process each multibyte character and reverse it
  for (auto it = word.rbegin(); it != word.rend(); ) {
    switch ((*it) & 0xf0) {
      case 0x00:
      case 0x10:
      case 0x20:
      case 0x30:
      case 0x40:
      case 0x50:
      case 0x60:
      case 0x70:
        //one byte
        ++it;
        --num_chars;
        break;
      case 0x80:
      case 0x90:
      case 0xa0:
      case 0xb0:
        HUNSPELL_WARNING(stderr,
                         "UTF-8 encoding error. Unexpected continuation bytes "
                         "in %ld. character position\n%s\n",
                         static_cast<long>(std::distance(word.begin(), it.base()) - 1),
                         word.c_str());
        ++it;
        --num_chars;
        break;
      case 0xc0:
      case 0xd0: {
	//two bytes
        if (num_chars >= 2) {
          std::iter_swap(it, it + 1);
          it += 2;
          num_chars -= 2;
        } else {
          HUNSPELL_WARNING(stderr,
                         "UTF-8 encoding error. Missing character at the end\n%s\n",
                         word.c_str());
          ++it;
          --num_chars;
        }
        break;
      }
      case 0xe0: {
        //three bytes
        if (num_chars >= 3) {
          std::iter_swap(it, it + 2);
          it += 3;
          num_chars -= 3;
        } else {
          HUNSPELL_WARNING(stderr,
                         "UTF-8 encoding error. Missing character at the end\n%s\n",
                         word.c_str());
          ++it;
          --num_chars;
        }
        break;
      }
      default: {
        // 4 or more byte UTF-8 codes
        if (num_chars >= 4) {
          std::iter_swap(it, it + 3);
          std::iter_swap(it + 1, it + 2);
          it += 4;
          num_chars -= 4;
        } else {
          HUNSPELL_WARNING(stderr,
                         "UTF-8 encoding error. Missing character at the end\n%s\n",
                         word.c_str());
          ++it;
          --num_chars;
        }
        break;
      }
    }
  }
  return word.size();
}

void uniqlist(std::vector<std::string>& list) {
  if (list.size() < 2)
    return;

  std::vector<std::string> ret;
  ret.push_back(list[0]);

  for (size_t i = 1; i < list.size(); ++i) {
    if (std::find(ret.begin(), ret.end(), list[i]) == ret.end())
        ret.push_back(list[i]);
  }

  list.swap(ret);
}

