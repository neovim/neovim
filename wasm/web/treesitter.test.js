// wasm/web/treesitter.test.js - tree-sitter grammars in the wasm engine.
//
// Two mechanisms are under test:
//
//  1. BUNDLED grammars. The native build ships them as dlopen()able shared
//     objects under lib/nvim/parser/*.so; wasm statically links the same
//     parsers into nvim.wasm and registers them as builtins (see
//     src/nvim/lua/treesitter.c + the EMSCRIPTEN block in
//     src/nvim/CMakeLists.txt). Opening a .lua file must start tree-sitter
//     highlighting (runtime/ftplugin/lua.lua calls vim.treesitter.start()
//     unconditionally) instead of throwing "Parser could not be created".
//
//  2. DYNAMIC grammars. The engine is linked -sMAIN_MODULE=2, so third-party
//     grammar .wasm files -- emscripten side modules, the artifact
//     `tree-sitter build --wasm` publishes -- load at runtime through nvim's
//     normal dlopen path: by explicit path, by runtimepath discovery
//     (parser/<lang>.wasm), or fetched from the host via the
//     create({parsers}) hook (globalThis.__nvimParserFetch). The fixture
//     (fixtures/luadyn.wasm, regenerated by fixtures/build-fixture.sh) is the
//     lua grammar renamed tree_sitter_luadyn -- a language name the builtin
//     registry can never satisfy.
//
// Prereqs: a finished wasm/build-nvim.sh (build-wasm/bin/nvim.{js,wasm}) and,
// for the worker-path check, `npm install` in wasm/web + dist/ built (both
// handled by `npm test`, which runs this after e2e.test.js).
// Run:  node wasm/web/treesitter.test.js   (Node >= 24, or 22 with
//                                            --experimental-wasm-jspi)
'use strict';

const path = require('path');
const fs = require('fs');
const os = require('os');
const http = require('http');
const { execFileSync } = require('child_process');

const ROOT = path.resolve(__dirname, '..', '..');
const BIN = path.join(ROOT, 'build-wasm', 'bin');
const NVIM_JS = path.join(BIN, 'nvim.js');
const FIXTURE = path.join(__dirname, 'fixtures', 'luadyn.wasm');

// ---- tiny test harness (same shape as e2e.test.js) --------------------------
let failures = 0;
function ok(cond, msg) {
  if (cond) { console.log('  ok   - ' + msg); }
  else { failures++; console.log('  FAIL - ' + msg); }
}
function fatal(msg) { console.error('treesitter: ' + msg); process.exit(1); }

if (!fs.existsSync(NVIM_JS)) { fatal('missing ' + NVIM_JS + ' (run wasm/build-nvim.sh)'); }
if (!fs.existsSync(FIXTURE)) { fatal('missing ' + FIXTURE + ' (run fixtures/build-fixture.sh)'); }

// Run the engine headless with a -l probe script; the probe reports one JSON
// line ('TSRESULT:{...}') on stdout. `wrapper` (optional) is JS prepended in a
// host script that require()s nvim.js -- used to install globals like the
// parser-fetch hook, exactly as a worker host would.
function runEngine(dir, probeSrc, probeArgs, wrapperPrelude) {
  const probe = path.join(dir, 'probe.lua');
  fs.writeFileSync(probe, probeSrc);
  const nvimArgs = ['--clean', '--headless', '-i', 'NONE',
    '--cmd', 'set noswapfile', '-l', probe].concat(probeArgs);
  const env = Object.assign({}, process.env, { NVIM_LOG_FILE: path.join(dir, 'nvim.log') });
  let argv;
  if (wrapperPrelude) {
    const host = path.join(dir, 'host.js');
    fs.writeFileSync(host,
      wrapperPrelude + '\n' +
      'globalThis.__nvimArgs = ' + JSON.stringify(nvimArgs) + ';\n' +
      'require(' + JSON.stringify(NVIM_JS) + ');\n');
    argv = [host];
  } else {
    argv = [NVIM_JS, '--'].concat(nvimArgs);
  }
  let stdout;
  try {
    stdout = execFileSync(process.execPath, argv,
      { encoding: 'utf8', timeout: 120000, stdio: ['ignore', 'pipe', 'pipe'], env: env });
  } catch (e) {
    stdout = (e.stdout || '') + '';
    if (!stdout.includes('TSRESULT:')) {
      fatal('engine run failed: ' + e.message + '\n' + (e.stderr || ''));
    }
  }
  const line = stdout.split('\n').find(function (l) { return l.startsWith('TSRESULT:'); });
  if (!line) { fatal('no TSRESULT line in engine output:\n' + stdout); }
  return JSON.parse(line.slice('TSRESULT:'.length));
}

function withTmpDir(fn) {
  const dir = fs.mkdtempSync(path.join(os.tmpdir(), 'nvim-wasm-ts-'));
  try { return fn(dir); }
  finally { fs.rmSync(dir, { recursive: true, force: true }); }
}

// ---- 1. bundled grammars ----------------------------------------------------

const BUNDLED_PROBE = `
local sample = _G.arg[1]
local out = {}

-- the raw loader: can the bundled lua grammar be registered at all?
local added, add_err = vim.treesitter.language.add('lua')
out.add_ok = added == true
out.add_err = add_err

-- the user path: open a .lua file; ftplugin/lua.lua runs
-- vim.treesitter.start() unconditionally.
local edit_ok, edit_err = pcall(vim.cmd, 'edit ' .. sample)
out.edit_ok = edit_ok
out.edit_err = edit_ok and nil or tostring(edit_err)

if edit_ok then
  local buf = vim.api.nvim_get_current_buf()
  out.highlighter_active = vim.treesitter.highlighter.active[buf] ~= nil
  local pok, parser = pcall(vim.treesitter.get_parser, buf, 'lua')
  if pok and parser then
    local tree = parser:parse()[1]
    out.root_type = tree and tree:root():type() or nil
  end
  local cok, caps = pcall(vim.treesitter.get_captures_at_pos, buf, 0, 0)
  out.captures = {}
  if cok then
    for _, c in ipairs(caps) do table.insert(out.captures, c.capture) end
  end
end

-- every grammar the native build bundles as parser/<lang>.so must be available
-- (keep in sync with builtin_langs[] in src/nvim/lua/treesitter.c).
out.bundled_failures = {}
for _, l in ipairs({ 'c', 'lua', 'markdown', 'markdown_inline', 'query', 'vim', 'vimdoc' }) do
  local lok = vim.treesitter.language.add(l)
  if lok ~= true then table.insert(out.bundled_failures, l) end
end

-- a grammar that is neither bundled nor on disk must still fail cleanly.
local missing_ok, missing_err = vim.treesitter.language.add('nosuchlang')
out.missing_ok = missing_ok == true
out.missing_err = missing_err

io.write('TSRESULT:' .. vim.json.encode(out) .. '\\n')
`;

console.log('# bundled grammars (statically linked)');
withTmpDir(function (dir) {
  const sample = path.join(dir, 'sample.lua');
  fs.writeFileSync(sample, 'local x = 1\nprint(x)\n');
  const r = runEngine(dir, BUNDLED_PROBE, [sample]);

  ok(r.add_ok === true, 'vim.treesitter.language.add("lua") succeeds' +
    (r.add_err ? ' (got error: ' + r.add_err + ')' : ''));
  ok(r.edit_ok === true, 'opening a .lua file raises no treesitter error' +
    (r.edit_err ? ' (got: ' + r.edit_err + ')' : ''));
  ok(r.highlighter_active === true, 'treesitter highlighter is active on the buffer');
  ok(r.root_type === 'chunk', 'parser produced a syntax tree (root=' + r.root_type + ')');
  ok(Array.isArray(r.captures) && r.captures.length > 0,
    'highlight captures resolve at "local" (' + JSON.stringify(r.captures) + ')');
  ok(Array.isArray(r.bundled_failures) && r.bundled_failures.length === 0,
    'all bundled grammars load (failed: ' + JSON.stringify(r.bundled_failures) + ')');
  ok(r.missing_ok === false && /No parser for language/.test(r.missing_err || ''),
    'a non-bundled grammar still reports "No parser for language"');
});

// ---- 2. dynamic grammars (side-module dlopen) --------------------------------

const DYNAMIC_PROBE = `
local fixture = _G.arg[1]   -- .../luadyn.wasm (explicit-path load)
local rtp_dir = _G.arg[2]   -- dir with parser/luadyn_rtp.wasm (discovery)
local out = {}

-- (a) explicit path: language.add's opts.path, the nvim-treesitter-style API.
--     .wasm routes to the dlopen loader in this build (loadparser).
local aok, aerr = vim.treesitter.language.add('luadyn', { path = fixture })
out.path_ok = aok == true
out.path_err = aerr
if out.path_ok then
  local tree = vim.treesitter.get_string_parser('local x = 1', 'luadyn'):parse()[1]
  out.path_root = tree:root():type()
end

-- (b) runtimepath discovery: parser/<name>.wasm on 'runtimepath', the way a
--     user drops a prebuilt grammar into ~/.local/share/nvim/site/parser/.
--     (The fixture's entry symbol is tree_sitter_luadyn, so the differently
--     named file registers with a symbol_name override.)
vim.opt.runtimepath:append(rtp_dir)
local bok, berr = vim.treesitter.language.add('luadyn_rtp', { symbol_name = 'luadyn' })
out.rtp_ok = bok == true
out.rtp_err = berr

-- (c) with no fetch hook installed, an unknown grammar still fails cleanly.
local cok, cerr = vim.treesitter.language.add('luadyn_fetch')
out.nohook_ok = cok == true
out.nohook_err = cerr

io.write('TSRESULT:' .. vim.json.encode(out) .. '\\n')
`;

console.log('# dynamic grammars (side-module dlopen: path + runtimepath)');
withTmpDir(function (dir) {
  const fixture = path.join(dir, 'luadyn.wasm');
  fs.copyFileSync(FIXTURE, fixture);
  const rtp = path.join(dir, 'site');
  fs.mkdirSync(path.join(rtp, 'parser'), { recursive: true });
  fs.copyFileSync(FIXTURE, path.join(rtp, 'parser', 'luadyn_rtp.wasm'));

  const r = runEngine(dir, DYNAMIC_PROBE, [fixture, rtp]);
  ok(r.path_ok === true, 'explicit-path .wasm grammar dlopens (language.add{path=...})' +
    (r.path_err ? ' (got: ' + r.path_err + ')' : ''));
  ok(r.path_root === 'chunk', 'dynamically loaded grammar parses (root=' + r.path_root + ')');
  ok(r.rtp_ok === true, 'runtimepath parser/<lang>.wasm is discovered and loaded' +
    (r.rtp_err ? ' (got: ' + r.rtp_err + ')' : ''));
  ok(r.nohook_ok === false && /No parser for language/.test(r.nohook_err || ''),
    'without a fetch hook, unknown grammars still fail cleanly');
});

// ---- 3. host-fetched grammars (the create({parsers}) hook) -------------------

const FETCH_PROBE = `
local out = {}
-- nothing on the runtimepath satisfies 'luadyn'; language.add falls back to
-- the host hook (globalThis.__nvimParserFetch, installed by the wrapper), the
-- engine suspends via JSPI while the host "fetches", and dlopens the result.
local aok, aerr = vim.treesitter.language.add('luadyn')
out.fetch_ok = aok == true
out.fetch_err = aerr
if out.fetch_ok then
  local tree = vim.treesitter.get_string_parser('local x = 1', 'luadyn'):parse()[1]
  out.fetch_root = tree:root():type()
end
-- a lang the hook has no bytes for still errors normally.
local bok, berr = vim.treesitter.language.add('luadyn_missing')
out.miss_ok = bok == true
out.miss_err = berr
io.write('TSRESULT:' .. vim.json.encode(out) .. '\\n')
`;

console.log('# host-fetched grammars (__nvimParserFetch hook)');
withTmpDir(function (dir) {
  // The wrapper plays the role wasm/worker.js / engine-worker.js play for a
  // real create({parsers}) config: install the hook, then boot the engine.
  const wrapper =
    'globalThis.__nvimParserFetch = async function (lang) {\n' +
    '  if (lang !== "luadyn") { return null; }\n' +
    '  return require("fs").readFileSync(' + JSON.stringify(FIXTURE) + ');\n' +
    '};';
  const r = runEngine(dir, FETCH_PROBE, [], wrapper);
  ok(r.fetch_ok === true, 'host-fetched grammar loads (hook -> MEMFS -> dlopen)' +
    (r.fetch_err ? ' (got: ' + r.fetch_err + ')' : ''));
  ok(r.fetch_root === 'chunk', 'host-fetched grammar parses (root=' + r.fetch_root + ')');
  ok(r.miss_ok === false && /No parser for language/.test(r.miss_err || ''),
    'hook returning null keeps the normal "No parser" error');
});

// ---- 4. the real worker path: create()-style parsers config over HTTP --------
// Boots the engine in a Node worker_thread via wasm/worker.js with a
// { parsers: { baseUrl } } config (exactly what create({parsers}) threads
// through in the browser) and a real HTTP server standing in for the CDN.

async function workerFetchScenario() {
  console.log('# worker-path parsers config (workerData.parsers -> fetch -> dlopen)');
  const { Worker } = require('worker_threads');
  const MessagePack = require('@msgpack/msgpack');
  const Neovim = require('./dist/neovim.js');
  const WORKER = path.join(BIN, 'worker.js');

  // Serve the fixtures dir: GET /luadyn.wasm -> the grammar bytes.
  const server = http.createServer(function (req, res) {
    const f = path.join(__dirname, 'fixtures', path.basename(req.url || ''));
    if (!fs.existsSync(f)) { res.statusCode = 404; res.end(); return; }
    res.setHeader('content-type', 'application/wasm');
    res.end(fs.readFileSync(f));
  });
  await new Promise(function (r) { server.listen(0, '127.0.0.1', r); });
  const baseUrl = 'http://127.0.0.1:' + server.address().port;

  const env = Object.assign({}, process.env);
  delete env.NVIM_LOG_FILE;
  const worker = new Worker(WORKER, {
    workerData: { args: [], parsers: { baseUrl: baseUrl } },
    env: env, stdout: true, stderr: true,
  });
  const transport = {
    onMessage: null, onClose: null, onStatus: null,
    send: function (u8) { worker.postMessage(u8.buffer, [u8.buffer]); },
    close: function () { worker.terminate(); },
  };
  worker.on('message', function (m) {
    if (m instanceof ArrayBuffer) { if (transport.onMessage) { transport.onMessage(new Uint8Array(m)); } }
  });
  worker.on('exit', function () { if (transport.onClose) { transport.onClose(); } });

  try {
    const nvim = Neovim.createNvim({ transport: transport, MessagePack: MessagePack });
    await nvim.ready;
    const added = await nvim.request('nvim_exec_lua',
      ['return (vim.treesitter.language.add("luadyn"))', []]);
    ok(added === true, 'worker parsers config: language.add fetched over HTTP (' + baseUrl + ')');
    const root = await nvim.request('nvim_exec_lua',
      ['return vim.treesitter.get_string_parser("local x = 1", "luadyn"):parse()[1]:root():type()', []]);
    ok(root === 'chunk', 'worker parsers config: fetched grammar parses (root=' + root + ')');
    nvim.dispose();
  } finally {
    await worker.terminate();
    server.close();
  }
}

(async function () {
  // The worker-path scenario needs dist/ + node_modules (npm test provides
  // them); skip gracefully when run standalone without them.
  if (fs.existsSync(path.join(__dirname, 'dist', 'neovim.js')) &&
      fs.existsSync(path.join(__dirname, 'node_modules', '@msgpack'))) {
    await workerFetchScenario();
  } else {
    console.log('# worker-path parsers config: SKIPPED (need wasm/web dist/ + npm install)');
  }
  if (failures) { console.log(failures + ' check(s) FAILED'); process.exit(1); }
  console.log('ALL CHECKS PASSED');
  process.exit(0);
})();
